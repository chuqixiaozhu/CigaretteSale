package com.etc.geeks.entity.StorageManagement;

public class InStoreGuest {
	private String inStoreGuestId;//入库信息从表ID
	private String inStoreHostId;//入库信息主表ID
	private String goodsId;//商品ID
	private String unit;//计量单位
	
	private int number;//数量
	private double price;//单价
	private double amount;//金额
	public String getInStoreGuestId() {
		return inStoreGuestId;
	}
	public void setInStoreGuestId(String inStoreGuestId) {
		this.inStoreGuestId = inStoreGuestId;
	}
	public String getInStoreHostId() {
		return inStoreHostId;
	}
	public void setInStoreHostId(String inStoreHostId) {
		this.inStoreHostId = inStoreHostId;
	}
	public String getGoodsId() {
		return goodsId;
	}
	public void setGoodsId(String goodsId) {
		this.goodsId = goodsId;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public double getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	@Override
	public String toString() {
		return "InStoreGuest [amount=" + amount + ", goodsId=" + goodsId
				+ ", inStoreGuestId=" + inStoreGuestId + ", inStoreHostId="
				+ inStoreHostId + ", number=" + number + ", price=" + price
				+ ", unit=" + unit + "]";
	}
	public InStoreGuest() {
		super();
		// TODO Auto-generated constructor stub
	}
	public InStoreGuest(String inStoreGuestId, String inStoreHostId,
			String goodsId, String unit, int number, double price, double amount) {
		super();
		this.inStoreGuestId = inStoreGuestId;
		this.inStoreHostId = inStoreHostId;
		this.goodsId = goodsId;
		this.unit = unit;
		this.number = number;
		this.price = price;
		this.amount = amount;
	}


	
}
