package com.etc.geeks.dao.GoodsInformation;

import java.util.List;

import com.etc.geeks.entity.GoodsInformation.GoodsSmallClass;

public interface GoodsSmallClassDao {
	/**
	 * 添加一条记录
	 * @param user
	 * @return
	 */
	public abstract int addGoSmallCl(GoodsSmallClass goSmallCl);
	/**
	 * 删除某条特定ID对应的记录
	 * @param userId
	 * @return
	 */
	public abstract int deleteById(String goSmallClId);
	/**
	 * 更新某条记录，ID不能修改
	 * @param user
	 * @return
	 */
	public abstract int updateGoSmallCl(GoodsSmallClass goSmallCl);
	/**
	 * 返回由名称找到的对象
	 * @param userName
	 * @return
	 */
	public abstract GoodsSmallClass findByName(String goSmallClName);
	/**
	 * 返回由ID找到的对象
	 * @param userId
	 * @return
	 */
	public abstract GoodsSmallClass findById(String goSmallClId);
	/**
	 * 返回所有对象
	 * @return
	 */
	public abstract List<GoodsSmallClass> findAllGoSmallCl();
}
